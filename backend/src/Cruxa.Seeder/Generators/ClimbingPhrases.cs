    public static string RandomRouteName() => Pick(RouteNames);

    public static string RandomRouteName(HashSet<string> used)
    {
        // Find a name not already used
        var pool = RouteNames.Where(n => !used.Contains(n)).ToList();
        if (pool.Count == 0)
        {
            // Fallback: just pick from the full pool anyway
            var name = Pick(RouteNames);
            used.Add(name);
            return name;
        }
        var chosen = pool[_rng.Next(pool.Count)];
        used.Add(chosen);
        return chosen;
    }

    public static string RandomPostDescription()
    {
        var sb = new StringBuilder();
        sb.Append(Pick(PostOpenings));
        sb.Append(' ');
        sb.Append(Pick(PostDetails));

        if (_rng.NextDouble() < 0.5)
        {
            sb.Append(' ');
            sb.Append(Pick(PostDetails));
        }

        sb.Append(' ');
        sb.Append(Pick(PostEndings));

        return sb.ToString();
    }

    private static string Pick(string[] pool) => pool[_rng.Next(pool.Length)];
